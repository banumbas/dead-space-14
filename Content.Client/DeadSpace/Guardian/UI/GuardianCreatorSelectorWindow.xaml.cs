using System.Numerics;
using Content.Client.Stylesheets;
using Content.Shared.DeadSpace.Guardian;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Prototypes;

namespace Content.Client.DeadSpace.Guardian.UI;

[GenerateTypedNameReferences]
public sealed partial class GuardianCreatorSelectorWindow : DefaultWindow
{
    private const float DetailsTextWidth = 430f;
    [Dependency] private readonly IPrototypeManager _prototype = default!;

    private readonly List<Button> _buttons = new();
    private readonly ButtonGroup _buttonGroup = new();
    private List<GuardianCreatorSelectorEntryState> _entries = new();
    private string? _selectedPrototype;

    public event Action<string>? Confirmed;

    public GuardianCreatorSelectorWindow()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        Title = Loc.GetString("guardian-selector-window-title");
        ConfirmButton.OnPressed += _ => ConfirmSelected();
    }

    public void UpdateState(GuardianCreatorSelectorBuiState state)
    {
        if (!OptionsEqual(_entries, state.Options))
        {
            _entries = state.Options;
            RebuildOptions();
        }

        if (_entries.Count == 0)
        {
            ClearDetails();
            return;
        }

        SelectEntry(Math.Clamp(state.SelectedIndex, 0, _entries.Count - 1));
    }

    private void RebuildOptions()
    {
        OptionsContainer.RemoveAllChildren();
        _buttons.Clear();

        for (var i = 0; i < _entries.Count; i++)
        {
            var index = i;
            var entry = _entries[index];
            var button = new Button
            {
                HorizontalExpand = true,
                MinHeight = 58,
                ToggleMode = true,
                Group = _buttonGroup,
                ToolTip = string.IsNullOrEmpty(entry.Description)
                    ? Loc.GetString(entry.Name)
                    : Loc.GetString(entry.Description),
                TooltipDelay = 0.01f,
            };

            button.OnPressed += _ => SelectEntry(index);
            button.AddChild(BuildOptionRow(entry));

            _buttons.Add(button);
            OptionsContainer.AddChild(button);
        }
    }

    private Control BuildOptionRow(GuardianCreatorSelectorEntryState entry)
    {
        var row = new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Horizontal,
            HorizontalExpand = true,
            SeparationOverride = 8,
            Margin = new Thickness(4, 3),
        };

        var preview = new EntityPrototypeView
        {
            SetSize = new Vector2(44, 44),
            Scale = new Vector2(2f, 2f),
            Stretch = SpriteView.StretchMode.Fill,
        };

        if (_prototype.TryIndex<EntityPrototype>(entry.Prototype, out var prototype))
            preview.SetPrototype(prototype);

        row.AddChild(preview);
        row.AddChild(new Label
        {
            Text = Loc.GetString(entry.Name),
            VerticalAlignment = VAlignment.Center,
            HorizontalExpand = true,
        });

        return row;
    }

    private void SelectEntry(int index)
    {
        if (index < 0 || index >= _entries.Count)
            return;

        var entry = _entries[index];
        _selectedPrototype = entry.Prototype;

        for (var i = 0; i < _buttons.Count; i++)
            _buttons[i].Pressed = i == index;

        NameLabel.Text = Loc.GetString(entry.Name);
        var summary = string.IsNullOrEmpty(entry.Description)
            ? string.Empty
            : Loc.GetString(entry.Description);
        SummaryLabel.SetMessage(summary);
        SummaryLabel.Visible = !string.IsNullOrWhiteSpace(summary);
        BuildDetails(entry);
        ConfirmButton.Disabled = false;

        if (_prototype.TryIndex<EntityPrototype>(entry.Prototype, out var prototype))
            Preview.SetPrototype(prototype);
        else
            Preview.SetPrototype(null);
    }

    private void ClearDetails()
    {
        _selectedPrototype = null;
        NameLabel.Text = Loc.GetString("guardian-selector-no-selection");
        SummaryLabel.SetMessage(string.Empty);
        SummaryLabel.Visible = false;
        DetailsContainer.RemoveAllChildren();
        Preview.SetPrototype(null);
        ConfirmButton.Disabled = true;
    }

    private void BuildDetails(GuardianCreatorSelectorEntryState entry)
    {
        DetailsContainer.RemoveAllChildren();

        var details = new RichTextLabel
        {
            SetWidth = DetailsTextWidth,
            StyleClasses = { StyleClass.LabelSubText },
        };

        details.SetMessage(Loc.GetString(entry.Details));
        DetailsContainer.AddChild(details);
    }

    private void ConfirmSelected()
    {
        if (_selectedPrototype is null)
            return;

        Confirmed?.Invoke(_selectedPrototype);
    }

    private static bool OptionsEqual(
        IReadOnlyList<GuardianCreatorSelectorEntryState> current,
        IReadOnlyList<GuardianCreatorSelectorEntryState> next)
    {
        if (current.Count != next.Count)
            return false;

        for (var i = 0; i < current.Count; i++)
        {
            var currentEntry = current[i];
            var nextEntry = next[i];

            if (!string.Equals(currentEntry.Prototype, nextEntry.Prototype, StringComparison.Ordinal) ||
                !string.Equals(currentEntry.Name, nextEntry.Name, StringComparison.Ordinal) ||
                !string.Equals(currentEntry.Description, nextEntry.Description, StringComparison.Ordinal) ||
                !string.Equals(currentEntry.Details, nextEntry.Details, StringComparison.Ordinal))
            {
                return false;
            }
        }

        return true;
    }
}
